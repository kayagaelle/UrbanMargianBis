package modele;

public abstract class Objet {

	protected int posX ;
	protected int posY ;
	protected Label label ;
	
	
	/**
	 * @return the posX
	 */
	public int getPosX() {
		return posX;
	}
	/**
	 * @return the posY
	 */
	public int getPosY() {
		return posY;
	}
	/**
	 * @return the label
	 */
	public Label getLabel() {
		return label;
	}
	
	
	
}
